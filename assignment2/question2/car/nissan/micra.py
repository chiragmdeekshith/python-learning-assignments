class Sunny:
    def __init__(self, colour, price):
        self.name = "Nissan Micra"
        self.year = 2010
        self.colour = colour
        self.price = price

    def print_details(self):
        print("Name: ", self.name)
        print("Year: ", self.year)
        print("Colour: ", self.colour)
        print("Price: ", self.price)
